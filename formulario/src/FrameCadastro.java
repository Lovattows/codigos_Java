    private void jBtnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnSalvarActionPerformed
        p.nome = jTFName.getText();
        p.telefone = jTFTelefone.getText();
        p.observacao = jTAObs.getText();
        if (jSelectWApp.isSelected()) {
            p.whatsapp = true;
        } else {
            p.whatsapp = false;
        }
        p.profissao = jComboProfissao.getSelectedItem() + "";
        if (jRadioMale.isSelected()) {
            p.genero = "Masculino";
        } else if (jRadioFemale.isSelected()) {
            p.genero = "Feminino";
        }
        a.salvar(p);
        JOptionPane.showMessageDialog(null, "Registrado com sucesso!!!");
        limparCampos();
    }//GEN-LAST:event_jBtnSalvarActionPerformed

