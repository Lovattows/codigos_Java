    private void jBtnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConsultarActionPerformed
        String vetor[] = a.consultar(jTFName.getText());
        jTFTelefone.setText(vetor[1]);
        if(vetor[3] != "Masculino"){
            jRadioMale.setSelected(true);
        }else if(vetor[3] != "Feminino"){
            jRadioFemale.setSelected(true);
        }
        jComboProfissao.setSelectedItem(vetor[5]);
    }//GEN-LAST:event_jBtnConsultarActionPerformed

